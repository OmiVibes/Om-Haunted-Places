        <p>Shaniwar Wada is a historical fortification in the city of Pune, India.

            Built in 1732,[1] it was the great seat of the Peshwas of the Maratha Empire until 1818. Following the rise
            of the Maratha Empire, the palace became the center of Indian politics in the 18th century.[2]

            The fort itself was largely destroyed in 1828 by an unexplained fire, but the surviving structures are now
            maintained as a tourist site.

            History

            An equestrian statue of Peshwa Baji Rao I, Prime Minister of the Maratha Empire, in the Shaniwar Wada
            complex. He was the first resident of the fortified palace.
            The Shaniwar Wada was normally the seven-story capital building of the Peshwas of the Maratha Empire. It was
            supposed to be made entirely of stone but after the completion of the base floor or the first story, the
            people of Satara (the national capital) complained to the Chatrapati Shahu Maharaj(King) saying that a stone
            monument can be sanctioned and built only by the king himself and not the Peshwas. Following this, an
            official letter was written to the Peshwas stating that the remaining building had to be made of brick and
            not stone. Even today if you visit and see the walls carefully, they are partly made of stone and partly by
            bricks. The Wada was then completed and upon being attacked by the British Artillery 90 years later, all the
            top six stories collapsed leaving only the stone base, which was immune to the British artillery. Hence only
            the stone base of the Shaniwar Wada remains and can be seen even today in the older parts of Pune.

            By 1758, at least a thousand people lived in the fort.

            In 1773, Narayanrao, who was the fifth and ruling Peshwa then, was murdered by guards on orders of his uncle
            Raghunathrao and aunt Anandibai. A popular legend has it that Narayanrao's ghost still calls for help on
            full moon nights. Various people, working around the area, have allegedly reported the cries [3][4] of "Kaka
            mala vachava"(Uncle, save me) by Narayanrao Peshwa after his death.

            In June 1818, the Peshwa, Bajirao II, abdicated his Gaadi (throne) to Sir John Malcolm of the British East
            India Company and went into political exile at Bithoor, near Kanpur in present-day Uttar Pradesh, India.

            On 27 February 1828, a great fire started inside the palace complex. The conflagration raged for seven days.
            Only the heavy granite ramparts, strong teak gateways and deep foundations and ruins of the buildings within
            the fort survived.[5]

            According to Haricharitramrutsagar, a biographical text of Bhagwan Swaminarayan, he had visited Shaniwarwada
            on the insistence of Bajirao II in 1799




