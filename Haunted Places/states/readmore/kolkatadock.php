            Port of Kolkata or Kolkata Port, officially known as Syama Prasad Mookerjee Port Trust (formerly Kolkata
            Port Trust), is the only riverine major port of India,[15] located in the city of Kolkata, West Bengal,
            around 203 kilometres (126 mi) from the sea.[16] It is the oldest operating port in India[17] and was
            constructed by the British East India Company.[18] Kolkata is a freshwater port with no variation in
            salinity.[19] The port has two distinct dock systems — Kolkata Dock at Kolkata and a deep water dock at
            Haldia Dock Complex, Haldia.

            In the 19th century, the Kolkata Port was the premier port in British India. After slavery was abolished in
            1833, there was a high demand for labourers on sugar cane plantations in the British Empire. From 1838 to
            1917, the British used this port to ship off over half a million Indians from all over India — mostly from
            the Hindi Belt (especially Bhojpur and Awadh) — and take them to places across the world, such as Mauritius,
            Fiji, South Africa, Trinidad and Tobago, Guyana, Suriname, and other Caribbean islands as indentured
            labourers. There are millions of Indo-Mauritians, Indo-Fijians, and Indo-Caribbean people in the world
            today.

            After independence, the port's importance decreased because of factors including the Partition of Bengal
            (1947), reduction in the size of the port hinterland, and economic stagnation in eastern India.

            It has a vast hinterland comprising the entire North East of India including West Bengal, Bihar, Jharkhand,
            Uttar Pradesh, Madhya Pradesh, Assam, North East Hill States and two landlocked neighbouring countries
            namely, Nepal and Bhutan and also the Autonomous Region of Tibet (China). With the turn of the 21st century,
            the volume of throughput has again started increasing steadily. As of March 2018, the port is capable of
            processing annually 650,000 containers, mostly from Nepal, Bhutan, and India's northeastern states.[18]

