            South Park Street Cemetery, formerly known as the ‘Great Christian Burial Ground’, was one of the earliest
            non-church cemeteries in the world.[1][2] The cemetery houses numerous graves and monuments belonging to
            British soldiers, administrators, and their families. It is also the final resting place of several
            prominent personalities, including Henry Louis Vivian Derozio and Sir William Jones. It is located on Mother
            Teresa Sarani (also known as Park Street), Central Kolkata, India.
            The burial ground, along with a new road leading to it, known as the Burial Ground Road, was built in 1767.
            The cemetery and the road were later renamed Park Street after a private deer park established by Sir Elijah
            Impey.[3] By the year 1785, the cemetery had been extended on the northern side of Park Street. Even though
            a marble plaque at the cemetery gate reads “South Park Street, Opened:1767, Closed:1790”, the burials
            continued in the 1830s,[2] until a vast new cemetery was opened to the east of the Lower Circular Road.

            After the independence of India, the Park Street cemeteries were decided to be leveled off, as funding for
            the colonial civil cemeteries were withdrawn. This decision met with resistance from Anglo-Indian citizens
            of Calcutta.[1] In 1953, the North Park Street Cemetery was decided to be demolished, and the income
            obtained by leasing the land was used to upkeep the South Park Street Cemetery.[1] In the late 1970s, the
            cemetery came under the purview of the Association for the Preservation of Historical Cemeteries in India
            (APHCI) and the Christian Burial Board.

            In 1984, Apeejay Trust attempted to demolish the cemetery in order to build an arts center, which was later
            stopped by an intervention from the Calcutta High Court.[4][5] Nevertheless, substantial damage had already
            been done by then, and many graves were lost. The grave of Charles Stuart was destroyed (and was later
            restored) and the demolition of Derozio's grave had been initiated. In the early 2000s, a restoration
            project was launched to conserve the cemetery's heritage and prevent its further deterioration
            The cemetery is considered to be an important example of colonial-era architecture, with its Gothic-style
            tombs and monuments, mixed with the rich flavor of the Greek, Egyptian, and Indo-Saracenic styles. In
            certain instances, elements from Hindu architecture and Islamic tombs have also been adopted.[6] Many of the
            monuments in the cemetery are adorned with intricate carvings, sculptures, and epitaphs. The structures are
            primarily constructed of sandstone, marble, and bricks, and feature a variety of shapes and sizes, ranging
            from simple headstones to grand mausoleums
