            Malcha Mahal, also known as Wilayat Mahal, is a Tughlak era hunting lodge in the Chanakyapuri area of New
            Delhi, India next to the Delhi Earth Station of the Indian Space Research Organisation.[1][2] It was built
            by Firuz Shah Tughlaq, who reigned over the Sultanate of Delhi, in 1325.[3] It came to be known as Wilayat
            Mahal after the self-proclaimed "Begum Wilayat Mahal" of Awadh, who claimed to be a member of the Royal
            family of Oudh and was reportedly given the place by the Government of India in May 1985.[1][4][5] On 10
            September 1993, Wilayat died by suicide at the age of 62.[6] The descendants of Wazid Ali Shah in Lucknow
            claim that the family engaged in fraudulent activities,[7] having been cited by an investigative journalist
            for the New York Times.[8]

            The building is now mostly in ruins. After Wilayat's death, it continued to be inhabited by the Begum's
            daughter Sakina Mahal, and son Prince Ali Raza (aka Cyrus). Cyrus died in late 2017; his sister died some
            years before him, though the exact date is unknown.
            Malcha Mahal is located in Malcha, one of the historical villages around Raisina Hill. Malcha, along with
            Raisina, Todapur, Aliganj, Pillanji, Jaisinghpura, and Kushak villages were moved by the British during the
            construction of capital New Delhi in the 1920s, especially the Viceroy's House, which is now known as the
            Rashtrapati Bhavan.[14]
            Begum Wilayat Mahal, self-proclaimed great-granddaughter of the last Nawab of Awadh, Wajid Ali Shah, was
            reportedly allotted the Mahal in May 1985, following the intervention of the Prime Minister of India, Indira
            Gandhi in 1984. Begum Wilayat Mahal had been protesting for nine years by living in a waiting room at the
            New Delhi railway station, demanding compensation for the loss of her ancestral property in Awadh which was
            seized when Wajid Ali Shah's kingdom was annexed by the British.[1][3][13] Begum Wilayat Mahal died by
            suicide in October 1993 and was survived by her two children.

            On 22 November 2019, the New England Bureau Chief of The New York Times, Ellen Barry, published a lengthy
            piece of investigative journalism in which she said she had discovered that Wilayat, in fact, had no
            connection to the Awadh royal family. Rather, she was the widow of the former Registrar of Lucknow
            University, Inayatullah Butt. Barry found her oldest son, Shahid Butt, living in the UK and he had told her
            the true story.[8]
            In late October of 2019, it was reported that INTACH has proposed to undertake restoration of Malcha
            Mahal.[15]

