        <p>The Howrah Bridge is a balanced cantilever bridge over the Hooghly River in West Bengal. Commissioned in
            1943,[9][11] the bridge was originally named the New Howrah Bridge, because it replaced a pontoon bridge at
            the same location linking the cities of Howrah and Kolkata (Calcutta). On 14 June 1965, it was renamed
            Rabindra Setu after the great Bengali poet Rabindranath Tagore, who was the first Indian and Asian Nobel
            laureate.[11] It is still popularly known as the Howrah Bridge.

            The bridge is one of four on the Hooghly River and is a famous symbol of Kolkata and West Bengal. The other
            bridges are the Vidyasagar Setu (popularly called the Second Hooghly Bridge), the Vivekananda Setu and the
            relatively new Nivedita Setu. It carries a daily traffic of approximately 100,000 vehicles[12] and possibly
            more than 150,000 pedestrians,[10] easily making it the busiest cantilever bridge in the world.[13] The
            third-longest cantilever bridge at the time of its construction,[14] the Howrah Bridge is currently the
            sixth-longest bridge of its type in the world.[15]\
            In view of the increasing traffic across the Hooghly river, a committee was appointed in 1855–56 to review
            alternatives for constructing a bridge across it.[17] The plan was shelved in 1859–60, to be revived in
            1868, when it was decided that a bridge should be constructed and a newly appointed trust vested to manage
            it. The Calcutta Port Trust was founded in 1870,[9] and the Legislative department of the then Government of
            Bengal passed the Howrah Bridge Act in the year 1871 under the Bengal Act IX of 1871,[9][17] empowering the
            lieutenant-governor to have the bridge constructed with Government capital under the aegis of the Port
            Commissioners.


            The Howrah Bridge Act of 1871
            Eventually a contract was signed with Sir Bradford Leslie to construct a pontoon bridge. Different parts of
            the bridge were constructed in England and shipped to Calcutta, where they were assembled. The assembling
            period was fraught with problems. The bridge was considerably damaged by the great cyclone on 20 March
            1874.[8] A steamer named Egeria broke from her moorings and collided head-on with the bridge, sinking three
            pontoons and damaging nearly 200 feet of the bridge.[8] The bridge was complete in 1874,[9] at a total cost
            of ₹2.2 million,[17] and opened to traffic on 17 October of that year.[8] The bridge was then 1528 ft long
            and 62 ft wide, with 7-foot wide pavements on either side.[9] Initially the bridge was periodically
            unfastened to allow steamers and other marine vehicles to pass through. Before 1906, the bridge used to be
            undone for the passage of vessels during daytime only. Since June of that year it started opening at night
            for all vessels except ocean steamers, which were required to pass through during daytime.[17] From 19
            August 1879, the bridge was illuminated by electric lamp-posts, powered by the dynamo at the Mullick Ghat
            Pumping Station.[9] As the bridge could not handle the rapidly increasing load, the Port Commissioners
            started planning in 1905 for a new improved bridge.

