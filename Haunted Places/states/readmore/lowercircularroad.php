        <p>Lower Circular Road Cemetery, also known as General Episcopal Cemetery, is located on the crossing of Mother
            Teresa Sarani (former Park Street) and Acharya Jagadish Chandra Bose Road (former Lower Circular Road),
            Kolkata, India, with its entrance on Acharya Jagadish Chandra Bose Road.[2]
            Lower Circular Road Cemetery was established on 1840 and is still operating as a functional cemetery. It
            contains approximately 12,000 graves including many former British East India Company employees.[3][4] There
            are two Second World War Commonwealth war graves, of an officer of the British Indian Army and a purser of
            the BOAC.[5]
            At one time, this cemetery contained a number of war graves of both world wars and although still in use in
            1953, the cemetery was very full and was expected to close shortly. All save two of the Second World War
            graves were therefore moved into the services plot in Calcutta (Bhowanipore) Cemetery. The remaining Second
            World War graves are those of an officer of the army of undivided India and a member of the British Overseas
            Airways Corporation. This organisation was one of several civilian organisations recognised by the Armed
            Forces, whose members' graves rank as war graves. Both graves have permanent private memorials and are
            privately maintained. Four First World War graves also remain in this cemetery, but as permanent maintenance
            cannot now be assured, the casualties are commemorated by name on the Madras 1914-18 War Memorial in Madras
            War Cemetery.
