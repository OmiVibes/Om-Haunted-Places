        <p>Delhi Ridge, sometimes simply called The Ridge, is a ridge in the Northern Aravalli leopard wildlife corridor
            in the National Capital Territory of Delhi in India.[1] It is a northern extension of the ancient Aravalli
            Range, some 1.5 billion years old (by comparison, the Himalayas are "only" 50 million years old).[2][3] The
            ridge consists of quartzite rocks and extends from the southeast at Tughlaqabad, near the Bhatti mines,
            branching out in places and tapering off in the north near Wazirabad on the west bank of the river
            Yamuna,[4] covering about 35 kilometres.[5]

            The Ridge acts as the "green lungs" for the city, and protects Delhi from the hot winds of the deserts of
            Rajasthan to the west. It has also enabled Delhi to be the world's second most bird-rich capital city, after
            Kenya's Nairobi.[6]

            Though modest in height, the ridge acts as a watershed dividing the Indus Plain to the west from the
            Gangetic Plain to the east, within the Indo-Gangetic plain.
            It is believed that the Aravallis are one of the oldest mountain ranges in India which evolved around 2.5
            billion years ago in the Archaeozoic times. Range extends from Gujarat through Rajasthan to Haryana-Delhi.
            In Delhi the spurs of the Aravallis are commonly called as the Delhi Ridge which is divided into the
            Northern, Central, South Central and Southern Ridge.[7]

            In 1993, parts of north Delhi, central Delhi, south West Delhi and south Delhi covering 7,777 hectares was
            declared a reserve forest. Thereafter in 1994 and 1996, a major part of the ridge was notified by the
            Government, thus stopping all construction.[8][9]

            Over the years, pressures of urban development, have seen forests of Delhi ridge under threat. In many
            areas, landscaped public parks, and public housing have come up, plus the area also faces dumping of
            construction waste.[10]
            The Ridge today, for administrative reasons, is divided into 4 separate zones,[11] namely:

            The Old Delhi or Northern Ridge denotes the hilly area near Delhi University and is by far the smallest
            segment of the Ridge. Northern Ridge location is 28°40′52″N 77°12′57″E. Nearly 170 hectares were declared a
            Reserved Forest in 1915. Less than 87 hectares remain today near Delhi University, which is being developed
            as the Northern ridge biodiversity park by the Delhi Development Authority.
            The New Delhi or Central Ridge was made into a Reserved Forest in 1914 and stretches from just south of
            Sadar Bazaar to Dhaula Kuan. It extends over 864 hectares, but some bits have been nibbled away.
            The Mehrauli or South-Central Ridge is centred on "Sanjay Vana", near JNU and Vasant Kunj, and encompasses
            633 hectares. Large chunks have been encroached and built upon. 70 hectares approximately near Sainik Farm
            are being developed as the Tilpath Valley Biodiversity Park.
            The Tughlaqabad or Southern Ridge sprawls across 6200 hectares and includes the Asola Bhatti Wildlife
            Sanctuary. This is the least urban of the 4 segments of the Ridge, but a lot of it is village-owned or
            privately owned farmland. This includes Bandhwari and Mangar Bani forests.
