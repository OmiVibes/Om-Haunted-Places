            Most of the ghats still exist between Baghbazar Canal in the north and Chandpal Ghat in the south. Many of
            the ancient ghats have even vanished, broken or turned dilapidated with the passage of time. Those who were
            entrusted with the job to maintain the ghats were either negligent or could not save the ghats from nature’s
            fury. Many ghats were discarded when they were beyond repair or lost their importance and newer ones were
            came up in their place. Some ghats have even been renamed like Raghu Mitra Ghat at Baghbazar has changed
            names to Annapurna Ghat.
            ot all ghats were constructed for the sole purpose of bathing. Some were constructed close to cremation
            grounds to facilitate cremation rites and rituals like Kashipur, Kashi Mitra Ghat and Nimtala Ghat. Some
            were constructed by the rich, elite class for bathing. There are unisex ghats where both males and females
            can bathe at the same time and yet there are some ghats earmarked for males and females separately. In
            Nimtola, for instance, Girish Chandra Basu Ghat is for females only and Radhamadhab Bandopadhyay Ghat is for
            males. Some ghats were constructed by rich businessmen for commercial purposes. These merchants utilized
            waterways extensively for trading and used the ghats to load and unload their goods from ships and boats.
            Some of the famous businessmen who had their private ghats constructed for this purpose include Madanmohan
            Dutta, Shobharam Basak, Baishnab Charan Seth, Kashinath Babu and Hujurimal. Ghats were constructed not only
            by native traders but British businessmen too, spent money to build ghats and expand their business. Some of
            these ghats built by British traders include Ross Bibir Ghat, Jackson Ghat, Foreman Ghat, Blyth Ghat and
            Smith Ghat.
            And some were used by the poor fishermen or Majhis. They were called ‘Ghat Majhi’. They stayed on the ghats
            and did not drive boats. Ghatmajhis were in charge of all business transactions at the ghat. They arranged
            for steamers, houseboats, ferry boats, dinghy boats, pontoon boats, deck boats etc according to requirements
            and supplied labourers for loading and unloading goods from ships. They provided boats and helmsmen for
            passengers to cross the river. They charged money from clients according to the services they provided. Many
            Ghatmajhi amassed huge wealth from this venture. There is a lane in Khidderpore that still bears the name of
            a Ghatmajhi: Nazir Mohammad Ghatmajhi Lane, at present, the shortened form of the name, Nazir Lane, is used.
