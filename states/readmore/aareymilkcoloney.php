            Updated: February 14, 2020 9:36 PM IST

            By Kriti Saraswat Satpathy


            Picture for representational purpose only.

            We continue with our ongoing series of the most haunted places in India where we tell you about the
            spookiest of places in the country that have an element of supernatural power. Over the past few weeks, we
            told you about the haunted Writer’s Building in Kolkata where no one goes after dark, the Bhangarh Fort in
            Rajasthan where no one is allowed to go after sunset. We also told you about Mumbai’s Mukesh Mills and the
            haunted Shaniwar Wada in Pune. Goa too has its fair share of haunted spots like the Three Kings Church and
            the Janki Bandh Bridge. And today, we add one more to this list – Aarey Milk Colony in Mumbai.
            Situated in the suburbs of Goregaon, this colony is known for its natural beauty and also boasts of a lake
            where families often come to picnic and enjoy the view. It is also known for its lush greenery and
            tree-lined pathways. But despite this, it Aarey Milk Colony is considered haunted and you will find its
            roads deserted at night. The strange, eerie feeling you get when you pass one of its roads is not new and
            many people will tell you they have experienced it not once but on several occasions.

            The colony is one of the fastest ways to reach Powai and often commuters go via it to avoid traffic. But a
            few spooky incidents have brought this number down. People say they’ve often heard the cries of children or
            the wailing of a woman. Many even claim to have spotted a lady asking for a lift and then disappearing. Many
            auto drivers refuse to take this road late at night for this very reason. Being in Goregaon, there are
            several film shoots that take place in and around the colony and often end at odd hours in the night. The
            stretch that is lined with trees is quite deserted and dimly-lit making it appear haunted even if it isn’t.
            Whether it is your mind playing tricks or there is indeed something supernatural, we don’t know but Aarey
            Milk Colony is considered one of the most haunted places in India. According to a leading daily’s report,
            cases of people performing black magic in the woods isn’t uncommon here despite a ban on it by the
            government.
