            A 10th-century Chudasama king[4][5] Graharipu (r. c.940-c.982)[6][7][8] cleared the old citadel free from
            the jungle. From the evidence contained in the Hemchandra's Dvyashraya, it can be concluded that Graharipu
            laid the foundations of the citadel as it now exists. the legend is told about its rediscovery..
            After several Chudasamas of Vamansthali had ruled, a woodcutter one day managed to cut his way through the
            forest and came to a place where stone walls and a gate existed. Nearby sat a holy man in contemplation, and
            on being asked by the woodcutter the name of the place and its history, he replied that its name was "Juna"
            — old. The woodcutter returned by the way he had come to Vamansthali, and reported his discovery to the
            Chudasama ruler, who ordered the forest to be cleared away. This being done, the fort came into sight. But
            there was none who knew its history, or who could tell more than the holy man had told the woodcutter. So
            the place became known as "Junagadh" for want of a better title.[9]

            If this story is to be believed, either Graharipu rediscovered an ancient stronghold or else after he had
            built the fort, it was abandoned and afterwards found again by a later ruler Navaghana who transferred
            Chudasama capital from Vamansthali to Junagadh.[9][3]
            The Uparkot is one of the most interesting of old forts. The parapets on the east, where the place is
            commanded by higher ground, have been raised at least three times to give cover against the increasingly
            long range of projectiles.[12]

            The entrance is beyond the town in the east wall, and consists of three gateways, one inside the other. The
            fort walls are from 60 to 70 feet high, forming a massive cluster of buildings. The inner gateway, a
            beautiful specimen of the Torana, has been topped by more later Indo-Saracenic work.[12]

            On the rampart above the gate is an inscription of Mandalika III, dated 1450.[12] Near this is the Jumma
            Masjid built by Mahmud Begada.[12]

            The Tomb of Nuri Shah, close to the mosque, is ornamented with fluted cupolas, and a most peculiar carving
            over the door. There are two Wells in the Uparkot — the Adi Kadi Vav, said to have been built in ancient
            times by the maids of the Chudasama rulers, is descended by a long flight of steps; and the Navghan Kuvo,
            cut to a great depth in the soft rock, and with a circular staircase.[12]

            Uparkot Caves are 2nd-3rd century Buddhist caves located in the Uparkot. It is double storeyed cave complex
            used by Buddhist monks during ancient times.[citation needed]

