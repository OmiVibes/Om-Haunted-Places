            The Royal Calcutta Turf Club (RCTC) is a horse racing organisation which was founded in 1847 in Calcutta,
            British India (now Kolkata). Horse events and sports were initially organised for the British cavalry at
            Akra before they were moved to the Maidan. The RCTC became the foremost horse-racing organization in India
            during the British Raj. At one time it was the governing body for nearly all racecourses in the
            subcontinent, defining and applying the rules governing the sport. During its heyday, RCTC-organised races
            were among the most important social events of the bigwigs' calendar and were opened by the Viceroy of
            India. Still a private club, the RCTC operates Kolkata Race Course in the Maidan.


            The CTC Grand Stand in 2015
            The club also held polo matches during the late 19th century, and hosted English-style gambling; the
            Calcutta Derby Sweeps, organised by the RCTC, was the world's largest sweepstake in the 1930s. After the
            closure of the Tollygunge racecourse, a new racecourse was opened by the club in Barrackpore during the
            1920s; it was unsuccessful due to poor attendance. Grandstands were built at the Maidan racecourse; Kolkata
            Race Course had three in 2020, including a three-tier main grandstand.
            Calcutta was the first base of British power in India. With its cavalry-based army, sports such as hunting,
            polo and racing were popular.[1] Organised horse races were first held in India on 16 January 1769 at Akra
            (near Calcutta), where they continued for the next 40 years.[2] Races were also organised in Garden Reach
            and Barasat.[3] The first races were on a rough, temporary course wide enough for only four horses.
            Reformist governor Lord Wellesley disapproved of organised racing and banned horse racing in 1798; but five
            years later, the Bengal Jockey Club was formed to resume racing at Akra. The races moved to the Calcutta
            Maidan in 1809, where they have remained. The club laid out a new course in the southwest part of the Maidan
            in 1812, at approximately present racecourse location.[4] A grandstand was built in 1820, which was later
            extensively modified.[5]

            Races, organised in the relative coolness of the morning (just after sunrise), usually consisted of five
            2.5-mile (4.0 km) heats. If a race was not decided in the morning, the heats resumed after sunset. The
            British press regularly published Calcutta race results. In 1825, the Calcutta Welter (India's main
            horse-racing event) was moved to the new course. The Calcutta Derby Stakes, in which maiden Arabians ran
            2.5-mile (4.0 km) for valuable prizes, began in 1842.[4]
