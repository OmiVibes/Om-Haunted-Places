            The Feroz Shah Kotla or Kotla ("fortress", "citadel") was a fortress built circa 1354 by Feroz Shah Tughlaq
            to house his version of Delhi city called Firozabad.[1]

            A pristine polished sandstone Topra Ashokan pillar from the 3rd century BC rises from the palace's crumbling
            remains, one of many pillars of Ashoka left by the Mauryan emperor; it was moved from Topra Kalan in Pong
            Ghati of Yamunanagar district in Haryana to Delhi under orders of Firoz Shah Tughlaq of Delhi Sultanate, and
            re-erected in its present location in 1356. The original inscription on the obelisk is primarily in Brahmi
            script but language was Prakrit, with some Pali and Sanskrit added later. The inscription was successfully
            translated in 1837 by James Prinsep.[2] This and other ancient lats (pillars, obelisk) have earned Firoz
            Shah Tughlaq and Delhi Sultanate fame for its architectural patronage.[3]

            Other than the Ashokan Pillar, the Fort complex also houses the Jami Masjid (Mosque), a Baoli and a large
            garden complex.
            Feroz Shah Tughlaq (r. 1351â€“1388), the Sultan of Delhi, established the fortified city of Firozabad[4] in
            1354, as the new capital of the Delhi Sultanate, and included in it the site of the present Feroz Shah
            Kotla. Kotla literally means fortress or citadel. The pillar, also called obelisk or Lat is an Ashoka
            Column, attributed to Mauryan ruler Ashoka. The 13.1 meters high column, made of polished sandstone and
            dating from the 3rd century BC, was brought from Ambala in the 14th century under orders of Feroz Shah. It
            was installed on a three-tiered arcaded pavilion near the congregational mosque, inside the Sultanate's
            fort. In centuries that followed, much of the structure and buildings near it were destroyed as subsequent
            rulers dismantled them and reused the spolia as building materials.[5][6]

            In the pre-independence era, due to lack of auditoriums in the capital, most classical music performances
            were staged here or at Qutub complex. Later Ebrahim Alkazi, then head of NSD, staged his landmark production
            of Dharamvir Bharati's Andha Yug here and its premiere in 1964 was attended by Prime Minister Jawaharlal
            Nehru.[7]
            Jami Masjid is one of the most ancient and largest surviving mosque and monument, still in use.
            Architecturally it was built on a series of underground cells made of quartzite stone, covered with
            limestone. It is surrounded by a large courtyard with cloisters and a Prayer Hall. The Prayer Hall now in
            complete ruins was once used by the Royal Ladies. The masjid and its architecture is an example of Tughluq
            architecture.

            The entrance of Jami Masjid lies on the northern side. It is connected by a causeway to the pyramidal
            structure of the Ashokan Pillar. This mosque was visited by Timur in 1398 AD to say his prayers. He was
            spellbound by its beauty and constructed a mosque in Samarkand in Mawarannahr imitating the design of this
            Masjid. This mosque is also known to be the place where Imad ul Mulk, a Mughal Prime Minister, got the
            Emperor Alamgir II murdered in 1759 AD.[9]
            The Ashokan Pillar which is now within Feroz Shah Kotla is towards the north of Jama Masjid [Mosque]. The
            Pillar was first erected by King Ashoka between 273 and 236 BC in Topra Kalan, Yamunanagar district,
            Haryana.[10]

            Of note, there is another Ashokan Pillar, that is seen installed near the Hindu Rao Hospital, also erected
            by Ashoka in Meerut. This pillar, however, was unfortunately broken into five pieces after it was damaged
            during an explosion. The pillar was neglected for a century up till 1838 when after the Revolt of 1857 Raja
            Hindu Rao took charge to transfer the Ashokan Pillar's broken pieces to Kolkata's Asiatic Society. Within a
            year, the structure was put together and re-established.

            Both the Ashokan Pillars were carefully wrapped with cotton silk and were kept on a bed of reed made of raw
            silk. These were hence transported on a massive carriage attached with 42 wheels and drawn meticulously by
            200 men from their original places to Delhi by Feroz Shah Tughlaq to avoid any damage during the journey.
            Upon reaching Delhi, they were then transported on huge boats to their final destination, one within Feroz
            Shah Kotla and the other on the ridge near Delhi University and Bara Hindu Rao Hospital.[9]
            The Sultanate wanted to break and reuse the Ashokan pillar for a minaret. Firoz Shah Tuhglaq, however,
            decided to erect it near the mosque instead. At the time of re-installation of the obelisk in Delhi, in
            1356, no one knew the meaning of the script engraved in the stone.[11]

            About five hundred years later, the script (Brahmi) was deciphered by James Prinsep in 1837 with help from
            scripts discovered on other pillars and tablets in South Asia.[2]
