        <p>
            Khooni Darwaza (Hindi: खूनी दरवाज़ा, Urdu: خونی دروازہ literally Bloody Gate), also referred to as Lal
            Darwaza (Hindi:लाल दरवाज़ा, Red Gate) was initially called as Kabuli Darwaza, The gate is located near Delhi
            Gate, on the Bahadur Shah Zafar Marg in Delhi, India. It is one of the 13 surviving gates in Delhi. It is
            just south of the fortified Old Delhi and was constructed by Sher Shah Suri.[1]
            Khooni Darwaza was situated on an open tract of land before the rise of modern buildings around it. It lies
            today on the Bahadur Shah Zafar Marg opposite the Feroz Shah Kotla cricket ground, which lies to its east.
            To the west is the entrance to the Maulana Azad Medical College. It lies about half a kilometre to the south
            of the Delhi Gate of Old Delhi.[1]
            Emperor Jahangir who succeeded his father Akbar to the throne, was opposed by some of Akbar's Navaratnas. He
            ordered that two sons of Abdul Rahim Khan-I-Khana, one of the Navratnas, be executed at the Khooni Darwaza.
            Their bodies were left to rot at the gate.[1]
            Aurangzeb (Shah Jahan's son) defeated his elder brother Dara Shikoh in the struggle for the throne and had
            his head displayed at the gate.[1]
            The gate is supposed to have seen bloodshed in 1739 when Delhi was ransacked by Nadir Shah of Persia.[2]
            However, this is also disputed - according to some sources, this massacre occurred at another gate of the
            same name located in the Dariba locality of Chandni Chowk.[1]
            A few stories also refer to the place being called Khooni Darwaza during the Mughal reign but there is no
            record of any mention of the name before 1857.
            The Khooni Darwaza (Bloody Gate) is first found by name in literature after three princes of the Mughal
            dynasty - Bahadur Shah Zafar's sons Mirza Mughal and Mirza Khizr Sultan and grandson Mirza Abu Bakht, were
            shot by a British officer, Major William Hodson, on 22 September 1857 during the Indian Rebellion of 1857
            (also known as the Indian Mutiny of 1857 or the First war of independence). Hodson obtained the surrender of
            the Emperor, and the next day asked for an unconditional surrender from the three princes at Humayun's Tomb.
            Hodson arrested about 16 members of the Emperor's family and was transporting them from Humayun's Tomb in a
            bullock cart accompanied by a detachment of 100 "sowars" (Indian cavalrymen in the British service). On
            reaching this gate, he was stopped and surrounded by thousands of Muslims, with white cloth tied on their
            foreheads (a symbol for the shroud) Jehadis or Ghazis. Hodson later recalled, "I was surrounded on all sides
            by Ghazis as far as my eyes could see." It is said that Hodson ordered the three to get down at the spot,
            stripped them naked and shot them dead at point blank range.[3] The bodies were then taken away and put up
            for public display for three days in front of a Kotwali near Chandni Chowk.[1]

            The Khooni Darwaza was an archway at the time of the revolt of 1857 and not a gate in the traditional
            sense.[4] It is often mistaken for the original Kabuli Gate of Old Delhi.[by whom?]
            During the riots of 1947, more bloodshed occurred near the gate when several refugees going to the camp
            established in Purana Qila were killed here.[1]

            Khooni Darwaza is today a protected monument under the aegis of the Archaeological Survey of India.

            It gained more notoriety in November 2002 when a medical student was raped there by three youths.[5] The
            incident sparked much uproar and was also discussed in the Parliament of India.[6] Following the incident,
            the monument was sealed to the general public.

