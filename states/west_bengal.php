            <div>
                <img src="https://static.toiimg.com/thumb/93049420/Putulbari-house-of-dolls.jpg?width=1200&height=900" alt="">
                <p>The name itself does half the job of spooking people out! This century-old building built after British architecture gets its name from the dolls on the arches, which used to be the residence of rich landlords. The history behind this building and the reported screams of cry by female voices here stems from the fact that women were tortured here. Even though there are people who have made the ground floor of the building their home, even they advise against going up the stairs. It’s a spook-fest, to say the least.
