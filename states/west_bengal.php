                <p>The Writer’s Building in Binoy Badal Dinesh Bagh is a place where the dead man apparently walks (not talking about The Undertaker). The person being mentioned is Captain Simpson of the East India Company, who was murdered here by revolutionaries during the struggle for independence. It is his ghost, which apparently walks here at night. Caretakers say that they heard shriek-like noises late in the night, even though the building is closed for people at 7:00 pm. No caretaker, it is claimed, has been able to continue his job for more than 24 hours!
